public class DataTypes {

    public static void main(String[] args) {
        boolean b = true;
        int a = 10;
        short s = 1200;
        byte by = 120;
        long l = 1023564;
        float f = 12.3f;
        double d = 1235.24;
        char ch = 'p';
        System.out.println(b);
        System.out.println(a);
        System.out.println(by);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);
    }
}