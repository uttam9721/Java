class factorial {

    public static int factt(int n){
        int f=1;
        for (int i = 0; i < n; i++) {
            f=f*i;
            
        }
        return f;
    }
    // System.out.println(")  
    }
    public static void main(String[] args) {
        
     System.out.println(factt(4));
        
}
