package Basic;

public class sumofNatural {
    public static void main(String[] args) {
        int sum = 0;
        int n=5;
        int i=1;
        while (i<=n) {
            // System.out.println(i);
            sum=sum+i;
            i++;
            
        }
        System.out.println(sum);
    }
    
}
